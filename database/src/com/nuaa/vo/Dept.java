package com.nuaa.vo;

public class Dept {
	private String DeptNum;//系编号
	private String DeptName;//系名称
	private String DeptChairman;//系主任
	private String DeptTel;//联系方式
	public String getDeptNum() {
		return DeptNum;
	}
	public void setDeptNum(String deptNum) {
		DeptNum = deptNum;
	}
	public String getDeptName() {
		return DeptName;
	}
	public void setDeptName(String deptName) {
		DeptName = deptName;
	}
	public String getDeptChairman() {
		return DeptChairman;
	}
	public void setDeptChairman(String deptChairman) {
		DeptChairman = deptChairman;
	}
	public String getDeptTel() {
		return DeptTel;
	}
	public void setDeptTel(String deptTel) {
		DeptTel = deptTel;
	}
	
}
